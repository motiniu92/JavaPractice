package com.coderbd.loops;

public class ForLoops {
    public static void main(String[] args) {

        System.out.println("pring 1 to 10");
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }

        System.out.println();
        System.out.println("print 10 to 1 :");
        for (int j = 10; j>0;j--){
            System.out.println(j);
        }
    }
}
