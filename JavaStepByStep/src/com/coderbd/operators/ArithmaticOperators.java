package com.coderbd.operators;

public class ArithmaticOperators {
    public static void main(String[] args) {
        System.out.println(10*10/5+3-1*4/2);

        // declare variables
        int a = 12, b = 5;

        // addition operator
        System.out.println("Additional Operator :");
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("Subtractional Operator :");
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("Multiplicational Operator :");
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("Divisional Operator :");
        System.out.println("a / b = " + (a / b));

        // modulo operator
        System.out.println("Modular Operator :");
        System.out.println("a % b = " + (a % b));
    }
}
